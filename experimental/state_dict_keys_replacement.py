from typing import List, Tuple


# 这种是局部替换,本身只替换两个框架中存在差异的层的名字,但是prefix这些是根据你定义的类名和变量名来的，你需要在两份代码中保持一致
# 这样可以做到最小替换
# 在dbnet中,我们只需要替换bn层的均值和方差``
def partial_dbnet_state_dict_keys_replacement() -> List[Tuple[str, str]]:
    """
    Returns:
        dbnet_replacement:paddle namespace -> torch namespace
            if src is empty,means that paddle did not have this,but torch have
            if dst is empty,means that paddle have this feature,but torch did not have
    """
    dbnet_replacement_list = []
    # bn层
    dbnet_replacement_list.extend(
        [
            ("_mean", "running_mean"),
            ("_variance", "running_var"),
            # paddle missing this feature
            ("", "num_batches_tracked")
        ]
    )
    return dbnet_replacement_list


